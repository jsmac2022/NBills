package com.nictbills.app.activities.tbo.flight.model.citylist;

public class CityModel {
    String cityname;
    String citycode_word;

    String city_code;
    String countryname;
    String countrycode;


    public String getCityname() {
        return cityname;
    }

    public void setCityname(String cityname) {
        this.cityname = cityname;
    }

    public String getCitycode_word() {
        return citycode_word;
    }

    public void setCitycode_word(String citycode_word) {
        this.citycode_word = citycode_word;
    }

    public String getCity_code() {
        return city_code;
    }

    public void setCity_code(String city_code) {
        this.city_code = city_code;
    }

    public String getCountryname() {
        return countryname;
    }

    public void setCountryname(String countryname) {
        this.countryname = countryname;
    }

    public String getCountrycode() {
        return countrycode;
    }

    public void setCountrycode(String countrycode) {
        this.countrycode = countrycode;
    }

}
